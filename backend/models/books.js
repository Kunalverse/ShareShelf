const mongoose = require('mongoose');

const bookSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
});

const Book = mongoose.model('Book', bookSchema);

module.exports = Book;
